package it.unibo.soseng.acmesky.Json;

public class LMFlight {

	
	String part;
	String dest;
	String offc;
	String pric;
	String data;
	
	public LMFlight() {
		super();
	}
	
	public String getPart() {
		return part;
	}
	public void setPart(String part) {
		this.part = part;
	}
	public String getDest() {
		return dest;
	}
	public void setDest(String dest) {
		this.dest = dest;
	}
	public String getOffc() {
		return offc;
	}
	public void setOffc(String offc) {
		this.offc = offc;
	}
	public String getPric() {
		return pric;
	}
	public void setPric(String pric) {
		this.pric = pric;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	
	
}
